/*     % -- 占位符    */

console.log(8+0.8);                     //  数值运算
console.log("%d",8,0.8);                //  数值格式化输出
console.log("%d - %d",8.8,8.0);         //  含有特殊字符的格式化
console.log("%d",8+8,8-8,8*8,8/8);      //  数值运算
console.log("%d","8+8");
console.log(8/0);



